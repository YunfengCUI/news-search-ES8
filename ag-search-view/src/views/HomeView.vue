require("echarts-wordcloud");
const {TopHotWord} = require("@/api/search");
export default {
  name: 'HomeView',
  mounted() {
  },
  data() {
    const item = {
    };
    return {
      search: '',
      tableData: Array(20).fill(item),
      cloud_data: [],
      arr_obj :[],
      arr2: []
    }
  },
  methods: {
    /**
     * 获取热词
     */
    getWord(){
      TopHotWord().then(resp=>{
        this.arr_obj = resp.data.result
        this.cloud_data = Object.values(this.arr_obj)
        let arr = []
        let x ={'value':'','name':''}
        for (let i = 0; i < this.cloud_data.length; i++) {
          x.value = this.cloud_data[i].word_top
          x.name = this.cloud_data[i].words
          arr.push(x)
          x= {}
        }
        this.cloud_data = arr
        this.getIint()
      }).catch(err=>{
        this.$notify.error('出错了')
      })
    },
    getIint () {
      let wordsChart = this.$echarts.init(this.$refs.wordsChart)
      wordsChart.setOption({
          backgroundColor: '', // canvas背景颜色
          // canvas标题配置项
          title: {
              text: '',
              textStyle: {
                  fontSize: 14,
                  color: '#3B3E41',
                  fontWeight: 'normal'
              }
          },
          series: [
              {
                  type: 'wordCloud',
                  left: '-5%',                 // X轴偏移量
                  top: '0%',                  // Y轴偏移量
                  width: '600px',               // canvas宽度大小
                  height: '450px',              // canvas高度大小
                  sizeRange: [12, 50],         //  词典字体大小范围配置
                  rotationRange: [0, 0],       // 词典字体旋转角度配置，默认不旋转
                  gridSize: 20,                // 词典字体间距配置
                  layoutAnimation: true,       // 为false词典过度会阻塞
                  textStyle: {                 // 词典样式配置
                      normal: {
                          color() {
                              // 颜色随机渐变
                              let colors = ['#fe9a8bb3', '#fe9a8bb3', '#fe9a8b03', '#9E87FFb3', '#9E87FFb3', '#9E87FFb3', '#fe9a8bb3', '#fe9a8bb3', '#fe9a8bb3', '#73DDFF', '#58D5FF']
